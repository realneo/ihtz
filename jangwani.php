            <h3>Day 1 of the emergency response:</h3>
            <p>The Islamic Help team developed and implemented a strategy to distribute 5,700 packs of cooked meals to the flood victims. Islamic Help and their respective volunteers packed the cooked food, and effectively distributed 3,700 packs of food in targeted areas such as, YANGA Sports Club, Masji Islah, Mtambani B, Uhurru Mchanganyiko Primary School, Kinondoni and Morogoro Road Jangawani.</p>
            <h3>Day 2 of the emergency response: </h3>
            <p>By day 2 of the emergency floods, Islamic Help Tanzania used social network sites to tweet and post reports on the damage caused by floods and progress of the teams flood response, which triggered a positive reaction from Islamic Help volunteers and donors. A total of 6 individuals volunteered in assisting the team with packing and distributing the remaining 2,300 packs of food. Islamic Help additionally received donations from Islamic Foundation, DYCC and individual donors.</p>
            <h3>The final days of the emergency response:</h3>
            <p>Islamic Help began distribution of dry food and NFI (non-food-items). The Islamic help team along with the volunteers initiated the distribution of dry food and NFI. The team distributed blankets, dry food and medicines to the victims. A total of 2,000 packs of cooked food were distributed on day 3 and day 4. The media was present at YANGA sports club where the Islamic Help Team were interviewed on their work. Islamic Help Tanzania ended the response project on the 28th of December. The government then took over with the recovery and rehabilitation process</p>
