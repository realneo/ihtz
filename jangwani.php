            <h1>Jangwani Floods 2011</h1>
            <img src='images/slides/jangwani/jangwani floods.png' alt='' width="695"/>
            <p>A total of 12,000 people were affected by the flash flood that hit many parts of Dar Es Salaam in 2011. The floods caused widespread destruction of livelihood, property and infrastructure leaving thousands of people homeless. Local authorities confirmed deaths and, some witnesses reported seeing dead bodies floating in some streams. Islamic Help Tanzania assembled a flood response team alongside a dedicated team of various stake holders to assess the needs of aid victims in order to cope with the crisis; and hence food and non-food items were availed.</p>
            <p>Islamic Help Tanzania supported and helped 2,000-2,500 people, almost 20% of the total affected. Tzsh16,000,000 (66% of total raised) was raised by Islamic Help Tanzania through donations made by locals, while 34% of the funds for the project came from Islamic Help UK.</p>
