            <p>The first foundation stone of the Eco Mosque was laid in March 2012. The structure was strategically built on high ground allowing the flow of wind, sunlight and water collection so as to create an efficient building system using green technologies, including solar power, and ensuring efficient energy management.</p>
            <p>The Eco Mosque can accommodate a congregation of up to 160 people and the first prayers were held in Ramadan 2012.</p>
