            <p>The main features of the Eco Mosque are:</p>
            <ul>
                <li><span class="red">Less noise pollution</span> - the design of the mosque has eliminated the need for a microphone as the architects have created a natural echo in the prayer hall</li>
                <li><span class="red">Natural ventilation</span> - the large windows and ventilation allow enough light and air to circulate through the mosque, reducing the need for electrical fans</li>
                <li><span class="red">Solar energy</span> - solar energy is used for lights, fans and motors for water pumps.</li>
                <li><span class="red">Rain water harvesting</span> - there are three water collection tanks with a capacity to hold 2,000 litres each. Over the course of a year it is estimated that the tanks will collect approximately 95,800 litres of water. The water will be used for wudu (ablution) and washing</li>
                <li><span class="red">Re-use of Wudu water</span> - water management is an often overlooked component in most construction projects. If, on average one person uses about 1.5 litres of water when making wudu from the mosque, a total of 225 litres will be used per day. The waste water from the mosque is taken directly to the gardens and is re-used to irrigate a fruit and vegetable garden consisting of bananas and pumpkins.</li>
            </ul>
