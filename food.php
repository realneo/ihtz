            <h3 class="red">Food security in coastal region</h3>
            <p>The government of Tanzania accords high priority to improving the food security and nutritional standards of its people. This has been implemented through agricultural policy, food strategy and other programmes. The policy goals of the Tanzanian food security program are, &ldquo;ensuring adequacy of food supplies, maintaining safe supply, stability and security access to available supplies by all consumers according to their nutritional needs&rdquo;.&nbsp;</p>
            <p>In order to support this initiative, Islamic help Tanzania is currently organizing and trying to solve the food problem facing the coastal region in Tanzania, so far the focus is in Mkuranga district were the local population is facing a problem of insufficient harvest as a result of poor method of farming and harvesting techniques.</p>
            <p>The direct step that Islamic help has taken is to provide knowledge on permaculture were by heads of the local villages were provided with skills and knowledge on how to improve their farming skills, being how to make local manure, how to zone there farms and finally how to yield better harvests.</p>
            <p>The next step will be providing seeds and equipment&rsquo;s that will be both effective and eco-friendly.</p>
