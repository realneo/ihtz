            <h1>Small Hands</h1>
            <p>Islamic Help Tanzania has always prioritized the need to help and empower the needy, as well as building their confidence and independence so as to promise a better future for communities. Small Hands is a project introduced by Islamic Help targeting young children (age 12 and under). Our Small Hands project integrates children of different backgrounds, races, religion or status and provides them with an objective that promotes collective team work, and education regarding the ‘real world’. The project aims to effectively develop children’s confidence, increase environmental awareness, promote problem solving, leadership and management, team working and decision making skills - preparing our youth of today to become better citizens of tomorrow.</p>
            <h3>The ‘Small Hands’ Project</h3> 
            <hp>The Small Hands project started in September 2010 with 12 excited children aspiring to make a difference in the world. An introduction session was held at the Islamic Help offices in Dar es Salaam where the children brain stormed and exchanged ideas regarding where changes can be made to improve poverty driven conditions and how they propose to make a difference. </p>
            <img src="images/small_hands_header_pic.png" alt="" />
            
            <h3>Small Hands Deployments</h3> 
            <p>Islamic Help held its first deployment in November 2012. The group of Small Hands children went to Manzese to spend a day with orphans, play, interact, learn and experience their day to day practices and struggles. The visit was productive for the Small Hands children as they gained first-hand experience how other children their ages live. On arrival to Islamic Help offices their minds were pouring with ideas on what can be done to make the orphans lives a little easier with their “small hands”.</p>
            <img src="images/sm_1.png" alt="" />
            <img src="images/sm_2.png" alt="" />
            <p>After their first visit with the orphans, another discussion session was held at Islamic Help Tanzania offices where the children listed ideas and prioritized their time on fundraising and what they thought would change the orphan’s lives. In the followings weeks, the children spoke to their families and people in their communities, collecting clothes, and shoes. Overall the children raised funds of up-to <span class='red'> 780,000 Tshs</span> with which they bought stationeries and other necessities for their fellow children. They spent the day wrapping the gifts and personally delivering them to the orphans. </p>
            <img src="images/sm_4.png" alt="" />
            <img src="images/sm_6.png" alt="" />

            <p>With the success of the first deployment, Small Hands held its second deployment in February 2013. The children gathered again at Islamic Help Tanzania offices and contributed to a brain storming discussion on what to do next. A story of one of the orphans who was bitten by a poisonous insect in his sleep due to his living condition stimulated a positive response from the Small Hands kids. The Small Hand kids fundraised in order to buy mattresses for the orphan children in Kigamboni. They bought 20 mattresses, wrapped them and again personally delivered them to the orphans. The Small Hands children spent the day interacting, singing and playing games with the orphans and getting to know each other’s background and life style.</p>
